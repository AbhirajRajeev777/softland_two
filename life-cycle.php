<p class=para>
<span style=font-weight:bold>A WALK THROUGH THE LIFE CYCLE</span>
We had started our company as a public limited company in the year <span style=font-weight:bold>1995</span>. We were the first company in INDIA to come out with an Auto Paging Machines equipment called PAGE- IT for sending messages to a Pager through a telephone line by keying in the data and further without any human intervention. This product was the first of its kind and was widely accepted by the mobile operators like BPL, Mobi Link, Easy Call, and Hutch.
</p>
<p class=para>Later on we had come out with an Automatic Telegram Handling system, Gram-IT for sending and receiving telegrams for the Department of Telecom, currently BSNL. Further we had upgraded this system to enable their offline payment collection systems also. Now we are having an array or models for BSNL for their telephone revenue collection handling. The various models available are GramIT-TR, Mobile-TR, CellOne-Tr, GramIT-TRB, Mobile-TRB, and CellOne-TRB.</p>
<p class=para>We have supplied more than 4000 numbers of off-line payment collection terminals to BSNL & Postal services. Client base of this product spread across all over India, from Kanyakumari to Sri Nagar and from Rajkot to Shillong.</p>
<p class=para>In <span style=font-weight:bold>1999 - 2000</span> we entered into designing of hand-held computer. Our hand held computer - Palmtec Big B is having in-built Printer & PC interface. It is very rugged, and hence suitable for the rough & diverse environment in India. Palmtec Big B weighs around 500 - 800 grams and runs on a rechargeable battery. We have an excellent variance of Palmtec models with Thermal Printer, Barcode Scanner, PC Keyboard connectivity, IR connectivity, Smart Card etc.</p>
<p class=para>Subsequent to the launch of the Palmtec, Big B, we had launched Palmtec Amphibia and Palmtec Plutonia , miniature versions of the Handheld Computer for an array of field applications.</p>
<p class=para>A stand-alone small footprint Printer, PRINTMARVEL with in built Rechargeable battery and Serial interface was also launched by us.</p>
<p class=para>BillTrac, a Cost Conscious Handheld Computer with Billing Machine Software, is another latest launch by us.
The Launch of Intelligent Printer iPrintMarvel has come out to be an Emerging Technology for the market for various Mobile Automation solutions.
</p>
